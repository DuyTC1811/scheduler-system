package org.example.model;

public class Mobiles {
    private String brand;
    private String model;
    private String weight;
    private String ram;
    private String frontCamera;
    private String backCamera;
    private String chip;
    private String battery;
    private String displaySize;
    private String pricePKR;
    private String priceINR;
    private String priceCNY;
    private String priceUSD;
    private String priceAED;
    private String releaseYear;

    public Mobiles() {
    }

    public String getBrand() {
        return brand;
    }

    public void setBrand(String brand) {
        this.brand = brand;
    }

    public String getModel() {
        return model;
    }

    public void setModel(String model) {
        this.model = model;
    }

    public String getWeight() {
        return weight;
    }

    public void setWeight(String weight) {
        this.weight = weight;
    }

    public String getRam() {
        return ram;
    }

    public void setRam(String ram) {
        this.ram = ram;
    }

    public String getFrontCamera() {
        return frontCamera;
    }

    public void setFrontCamera(String frontCamera) {
        this.frontCamera = frontCamera;
    }

    public String getBackCamera() {
        return backCamera;
    }

    public void setBackCamera(String backCamera) {
        this.backCamera = backCamera;
    }

    public String getChip() {
        return chip;
    }

    public void setChip(String chip) {
        this.chip = chip;
    }

    public String getBattery() {
        return battery;
    }

    public void setBattery(String battery) {
        this.battery = battery;
    }

    public String getDisplaySize() {
        return displaySize;
    }

    public void setDisplaySize(String displaySize) {
        this.displaySize = displaySize;
    }

    public String getPricePKR() {
        return pricePKR;
    }

    public void setPricePKR(String pricePKR) {
        this.pricePKR = pricePKR;
    }

    public String getPriceINR() {
        return priceINR;
    }

    public void setPriceINR(String priceINR) {
        this.priceINR = priceINR;
    }

    public String getPriceCNY() {
        return priceCNY;
    }

    public void setPriceCNY(String priceCNY) {
        this.priceCNY = priceCNY;
    }

    public String getPriceUSD() {
        return priceUSD;
    }

    public void setPriceUSD(String priceUSD) {
        this.priceUSD = priceUSD;
    }

    public String getPriceAED() {
        return priceAED;
    }

    public void setPriceAED(String priceAED) {
        this.priceAED = priceAED;
    }

    public String getReleaseYear() {
        return releaseYear;
    }

    public void setReleaseYear(String releaseYear) {
        this.releaseYear = releaseYear;
    }
}
